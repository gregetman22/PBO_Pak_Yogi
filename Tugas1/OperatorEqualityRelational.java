public class OperatorEqualityRelational {
    public static void main(String[] args) {
        int a = 9;
        int b = 10;
        
        System.out.println(a == b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a == 9);
        System.out.println(b == 10);
    }
}
